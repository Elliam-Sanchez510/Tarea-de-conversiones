// calcular area de un cuadrado //

let lado = 10
let centimetros = (lado * lado )
console.log(` El area del cuadrado de ${lado} centimetros es de: ${centimetros} centimetros².`);


// calcular el área de un circulo //

let radio = 5
let pi = (Math.PI * radio * radio)
console.log(` El area del circulo de ${radio} centimetros es de: ${pi} centimetros.`);


// calcular el área de un triangulo rectángulo //

let base = 20
let  altura  = 5
let area = (base*altura) /2
console.log(` El area del triangulo rectangulo es de:  ${area} centimetros.`);


